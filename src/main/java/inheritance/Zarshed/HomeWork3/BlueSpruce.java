package inheritance.Zarshed.HomeWork3;

public class BlueSpruce extends Tree{
    @Override
    public void cutting() {
        System.out.println("midsummer around early July");

    }

    @Override
    public void watered() {
        System.out.println("about 1 inch of water per week");
    }

    @Override
    public void describe() {
        System.out.println("conical-shaped evergreen tree with stiff horizontal branches and short stiff needles.");

    }

    @Override
    public void bloom() {
        System.out.println("Year-round growth; no flowers");

    }
}
